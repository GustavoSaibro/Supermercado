package Modelos;

/**
 *
 * @author Gustavo
 */
public class Funcionario {
    private String nome;
    private String senha;

    public Funcionario() {
    }

    public Funcionario(String nome, String senha) {
        this.nome = nome;
        this.senha = senha;
    }

    public String getNome() {
        return nome;
    }

    public String getSenha() {
        return senha;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
    
}
